class ProcessStructure {
   
   private String type;
   private int weight;
   
   public ProcessStructure(String type, int weight) {
       this.type = type;
       this.weight = weight;
   }
   
   /**
    * @return the type
    */
   public String getType() {
       return type;
   }

   /**
    * @return the weight
    */
   public int getWeight() {
       return weight;
   }
}
